@bot.on(zelda_cmd(outgoing=True, pattern=r"fs (typewriter|serif|smallcap|outline|san|slantsan|script|sim) (.*)"))
async def textstyle(event):
    if event.pattern_match.group(1) == "typewriter":
        cls = typewriter
    if event.pattern_match.group(1) == "serif":
        cls = serif
    if event.pattern_match.group(1) == "smallcap":
        cls = smallcap
    if event.pattern_match.group(1) == "outline":
        cls = outline
    if event.pattern_match.group(1) == "san":
        cls = san
    if event.pattern_match.group(1) == "slantsan":
        cls = slantsan
    if event.pattern_match.group(1) == "script":
        cls = script
    if event.pattern_match.group(1) == "sim":
        cls = sim
    msg = cls(event.pattern_match.group(2))
    await event.edit(f"{msg}")
    
    
